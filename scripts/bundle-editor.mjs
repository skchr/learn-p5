import { execSync } from "child_process";
import fs from "fs";
import path from "path";

const MIN_BUNDLE_BYTES = 100_000;

function main() {
  const outFile = path.resolve("src/utils/editor/codemirror-bundle.generated.ts");
  const entry = path.resolve("src/utils/editor/bundleEntry.ts");
  const esbuild = path.resolve("node_modules/.bin/esbuild");

  if (!fs.existsSync(esbuild)) {
    console.error(
      `\nError: esbuild not found at ${esbuild}.\nRun "npm install" first, or install esbuild globally.\n`
    );
    process.exit(1);
  }

  const result = execSync(
    `"${esbuild}" "${entry}" --bundle --format=iife --global-name=CM --minify --platform=browser --target=es2020`,
    { encoding: "utf-8", maxBuffer: 50 * 1024 * 1024 }
  );

  const bundle = result.trim();

  if (bundle.length < MIN_BUNDLE_BYTES) {
    console.error(
      `\nError: CodeMirror bundle is too small (${bundle.length} bytes, expected >= ${MIN_BUNDLE_BYTES}).\n` +
        "esbuild may have failed to bundle dependencies. Check node_modules.\n"
    );
    process.exit(1);
  }

  const tsContent = `// Auto-generated by scripts/bundle-editor.mjs — DO NOT EDIT
export const CODEMIRROR_BUNDLE = ${JSON.stringify(bundle)};
`;
  fs.writeFileSync(outFile, tsContent, "utf-8");
  console.log(`Editor bundle written to ${outFile} (${bundle.length} bytes)`);
}

main();
