import { execSync } from "child_process";
import fs from "fs";
import path from "path";

// Packages directly imported by the bridge scripts plus their transitive deps.
const PACKAGES = [
  // direct editor deps
  "codemirror@6.0.1",
  "@codemirror/state@6.5.2",
  "@codemirror/view@6.36.5",
  "@codemirror/language@6.11.0",
  "@codemirror/commands@6.8.1",
  "@codemirror/lang-javascript@6.2.3",
  "@codemirror/autocomplete@6.18.6",
  "@codemirror/lint@6.8.5",
  "@codemirror/search@6.5.10",
  "@lezer/common@1.2.3",
  "@lezer/lr@1.4.2",
  "@lezer/highlight@1.2.1",
  "@lezer/javascript@1.5.4",
  "style-mod@4.1.2",
  "w3c-keyname@2.2.8",
  "crelt@1.0.1",
  // transitive deps not pulled in by the above
  "@marijn/find-cluster-break@1.0.2",
];

function tarballUrl(spec) {
  // e.g. "codemirror@6.0.1" -> "https://registry.npmjs.org/codemirror/-/codemirror-6.0.1.tgz"
  // e.g. "@codemirror/state@6.5.2" -> "https://registry.npmjs.org/@codemirror/state/-/state-6.5.2.tgz"
  if (spec.startsWith("@")) {
    const [scope, rest] = spec.split("/");
    const [name, ver] = rest.split("@");
    return `https://registry.npmjs.org/${scope}/${name}/-/${name}-${ver}.tgz`;
  }
  const [name, ver] = spec.split("@");
  return `https://registry.npmjs.org/${name}/-/${name}-${ver}.tgz`;
}

function downloadAndExtract(spec, targetDir) {
  const url = tarballUrl(spec);
  const tmpDir = fs.mkdtempSync("npm-");
  const tgz = path.join(tmpDir, "pkg.tgz");

  execSync(`curl -sfL "${url}" -o "${tgz}" 2>/dev/null`, { encoding: "utf-8", timeout: 60000 });

  // Validate it's a gzip
  const buf = Buffer.alloc(2);
  const fd = fs.openSync(tgz, "r");
  fs.readSync(fd, buf, 0, 2, 0);
  fs.closeSync(fd);
  if (buf[0] !== 0x1f || buf[1] !== 0x8b) {
    throw new Error(`Invalid tarball (not gzip) for ${spec}`);
  }

  execSync(`tar -xzf "${tgz}" -C "${tmpDir}" 2>/dev/null`, { encoding: "utf-8" });

  // npm tarballs contain a single directory "package"
  const pkgDir = path.join(tmpDir, "package");
  const src = fs.existsSync(pkgDir) ? pkgDir : tmpDir;

  // Determine package name for node_modules path
  const name = spec.startsWith("@") ? spec.split("@").slice(0, 2).join("@") : spec.split("@")[0];
  const out = path.join(targetDir, "node_modules", name);
  fs.mkdirSync(path.dirname(out), { recursive: true });

  // Copy contents (skip node_modules if the package ships any)
  const files = fs.readdirSync(src);
  for (const f of files) {
    if (f !== "node_modules") {
      fs.cpSync(path.join(src, f), path.join(out, f), { recursive: true });
    }
  }

  fs.rmSync(tmpDir, { recursive: true, force: true });
}

function main() {
  const outFile = path.resolve("src/utils/editor/codemirror-bundle.generated.ts");
  const tmpDir = fs.mkdtempSync("cm-bundle-");

  try {
    for (const pkg of PACKAGES) {
      process.stdout.write(`Downloading ${pkg}... `);
      downloadAndExtract(pkg, tmpDir);
      console.log("done");
    }

    // Write entry point
    const entry = path.join(tmpDir, "entry.mjs");
    fs.writeFileSync(
      entry,
      `\
import { basicSetup, EditorView } from "codemirror";
import { EditorState } from "@codemirror/state";
import { keymap } from "@codemirror/view";
import { syntaxHighlighting, HighlightStyle } from "@codemirror/language";
import { indentSelection } from "@codemirror/commands";
import { javascript } from "@codemirror/lang-javascript";
import { tags } from "@lezer/highlight";
export { basicSetup, EditorView, EditorState, keymap, syntaxHighlighting, HighlightStyle, indentSelection, javascript, tags };
`,
      "utf-8"
    );

    console.log("Bundling with esbuild...");
    const result = execSync(
      `npx --yes esbuild entry.mjs --bundle --format=iife --global-name=CM --minify --platform=browser --target=es2020`,
      { encoding: "utf-8", maxBuffer: 50 * 1024 * 1024, cwd: tmpDir }
    );

    const tsContent = `// Auto-generated by scripts/bundle-editor.mjs — do not edit manually
export const CODEMIRROR_BUNDLE = ${JSON.stringify(result.trim())};
`;
    fs.writeFileSync(outFile, tsContent, "utf-8");
    console.log(`\nEditor bundle written to ${outFile} (${result.length} bytes)`);
  } catch (e) {
    console.error("\nBundle generation failed:", e.message);
    // Write placeholder so TypeScript imports don't break
    fs.writeFileSync(
      outFile,
      `\
// Bundle generation placeholder — install deps and run \`npm run bundle-editor\`.
export const CODEMIRROR_BUNDLE = "";
`,
      "utf-8"
    );
    console.log("Placeholder written to", outFile);
  } finally {
    fs.rmSync(tmpDir, { recursive: true, force: true });
  }
}

main();
