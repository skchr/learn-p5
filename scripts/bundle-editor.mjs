import { execSync } from "child_process";
import fs from "fs";
import path from "path";

function main() {
  const outFile = path.resolve("src/utils/editor/codemirror-bundle.generated.ts");
  const entry = path.resolve("src/utils/editor/bundleEntry.ts");
  const esbuild = path.resolve("node_modules/.bin/esbuild");

  try {
    const result = execSync(
      `"${esbuild}" "${entry}" --bundle --format=iife --global-name=CM --minify --platform=browser --target=es2020`,
      { encoding: "utf-8", maxBuffer: 50 * 1024 * 1024 }
    );

    const tsContent = `// Auto-generated by scripts/bundle-editor.mjs
export const CODEMIRROR_BUNDLE = ${JSON.stringify(result.trim())};
`;
    fs.writeFileSync(outFile, tsContent, "utf-8");
    console.log(`Editor bundle written to ${outFile} (${result.length} bytes)`);
  } catch (e) {
    console.error("\nBundle generation failed:", e.message);
    fs.writeFileSync(
      outFile,
      `\
// Bundle generation placeholder.
export const CODEMIRROR_BUNDLE = "";
`,
      "utf-8"
    );
    console.log("Placeholder written to", outFile);
  }
}

main();
